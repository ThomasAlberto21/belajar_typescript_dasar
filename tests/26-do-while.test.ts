describe('Do While Loop', () => {
  it('do while loop', () => {
    /**
     * - Sama seperti di JavaScript, di TypeScript juga kita bisa melakukan do while loop
     * - Do while loop di TypeScript sama seperti do while loop di JavaScript
     */

    let i: number = 0;
    do {
      console.log(i);
      i++;
    } while (i < 5);
  });
});
