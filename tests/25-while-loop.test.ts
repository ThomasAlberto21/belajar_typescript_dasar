describe('While Loop', () => {
  it('while loop', () => {
    /**
     * - Sama seperti di JavaScript, di TypeScript juga kita bisa melakukan while loop
     * - While loop di TypeScript sama seperti while loop di JavaScript
     */

    let i: number = 0;
    while (i < 5) {
      console.log(i);
      i++;
    }
  });
});
